
//  ADDITION ( + )
let number1 = 10, number2 = 10;
let results = number1 + number2;
console.log(results);

//  DECREMENT ( -- )
let number3 = 10;
number3--;
console.log(number3)

//  DIVISION ( / )
let number5 = 10, number6 = 2;
let results2 = number5 / number6;
console.log(results2);

//  EXPONENTIATION ( ** )
let number7 = 10;
let number8 = 10;
let results3 = number7 ** number8;
console.log(results3);

//  INCREMENTO ( ++ )
let number9 = 10;
number9++;
console.log(number9);

//  MULTIPLICATION ( * )
let number10 = 10;
let number11 = 10;
let results4 = number10 * number11;
console.log(results4);

//  REMAINDER ( % )
let number12 = 20;
let number13 = 2;
let results5 = number12 % number13
console.log(results5);

//  SUBTRACTION
let number14 = 10;
let number15 = 20;
let results6 = number14 - number15;
console.log(results6);

//  UNARY NEGATION
let number16 = -10;
console.log(number16);

//  UNARY PLUS
let number17 = +10;
console.log(number17);